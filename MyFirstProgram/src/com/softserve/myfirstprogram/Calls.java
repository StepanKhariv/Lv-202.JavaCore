public class Calls {
	void separateCall(int t1, int t2, int t3){
		System.out.println("C1 continue "+t1);
		System.out.println("C2 continue "+t2);
		System.out.println("C3 continue "+t3);
	}
	int getAllTalkTogether(int t1, int t2, int t3){
		return t1+t2+t3;
	}
}
